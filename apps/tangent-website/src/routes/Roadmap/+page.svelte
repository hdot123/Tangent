	<p>
		Tangent 正在积极开发中。
		你可以在
		<a href="https://github.com/orgs/suchnsuch/projects/2/" target="_blank">Github 项目</a>上关注进展，
		或查看活跃的
		<a href="https://github.com/suchnsuch/Tangent/issues" target="_blank">Issue</a>。
	</p>
	<p>
		以下是当前项目长期规划的概览。
	</p>
	<h1>近期计划</h1>
	<ul>
		<li><strong>导出</strong>笔记为 HTML 或简化 Markdown。</li>
		<li>
			<strong>行级查询</strong>
			– 搜索笔记中的单行和待办事项。
		</li>
	</ul>
	<h1>中期计划</h1>
	<ul>
		<li>
			<strong>看板式生产力</strong>
			– 在自由画板上可视化笔记，支持列表、表格和泳道结构。
		</li>
		<li>
			<strong>笔记重构控制</strong>
			– 通过强大的自动化功能将笔记内容提取到新笔记或现有笔记中。
		</li>
		<li>
			<strong>标签参数</strong>
			– 为标签添加附加信息。
		</li>
	</ul>
	<h1>远期计划</h1>
	<ul>
		<li><strong>在笔记旁查看网页和 PDF</strong>，支持可选的自动归档。</li>
		<li><strong>iOS 和 Android</strong> 支持。</li>
		<li><strong>协作写作</strong> 通过共享工作区。</li>
	</ul>

